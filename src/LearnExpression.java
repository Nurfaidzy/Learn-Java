public class LearnExpression {
    public static void main(String[] args) {
        // ekspression digunakan untuk melakukan pendefenisian dari variable
        int value;
        value = 10;
        System.out.print(value == 100);

        // statement
        // sytem.out.print() ini adalah ekspresi krena dalam print() ada statemen atau
        // dapat dikatakna bahwa ini merupakan kumpulan dari statement
        System.out.print(value == 100);

        // block dapat dikatakan sebagai block ketika di mulai dari  { hingg } 
     }
}
